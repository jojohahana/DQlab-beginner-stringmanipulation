# Fitur .capitalize() Mengubah elemen pertama menjadi kapital
print(">>> Fitur .capitalize()")
judul_buku = 'belajar bahasa Python'
print(judul_buku.capitalize())
# Fitur .lower() Mengubah seluruh elemen menjadi huruf kecil
print(">>> Fitur .lower()")
judul_buku = 'Belajar Bahasa PYTHON.'
print(judul_buku.lower())
# Fitur .upper() Mengubah seluruh elemen menjadi huruf besar
print(">>> Fitur .upper()")
judul_buku = 'Belajar Bahasa PYTHON.'
print(judul_buku.upper())