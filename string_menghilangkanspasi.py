# Fitur .strip() Menghilangkan kelebihan spasi pada awal dan akhir string
print(">>> Fitur .strip()")
kata_sambutan = ' halo, selamat siang! '
kata_sambutan = kata_sambutan.strip()
print(kata_sambutan)
# Fitur .lstrip() Menghilangkan kelebihan spasi pada awal string
print(">>> Fitur .lstrip()")
kata_sambutan = ' halo, selamat siang! '
kata_sambutan = kata_sambutan.lstrip() # left side
print(kata_sambutan)
# Fitur .rstrip() Menghilangkan kelebihan spasi pada akhir string
print(">>> Fitur .rstrip()")
kata_sambutan = ' halo, selamat siang! '
kata_sambutan = kata_sambutan.rstrip() # Right side
print(kata_sambutan)