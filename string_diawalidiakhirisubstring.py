# Fitur .startswith() Mengembalikan nilai kebenaran True ketika sebuah teks(string) diawali dengan sebuah teks lainnya || AWAL
print(">>> Fitur .startswith()")
teks = "Apel malang adalah apel termanis dibanding apel-apel lainnya"
print(teks.startswith("Apel"))
print(teks.startswith("apel"))
# Fitur .endswith() Mengembalikan nilai kebeneran True ketika string diakhiri dengan teks lainnya || AKHIR
print(">>> Fitur .endswith()")
print(teks.endswith("lainnya"))
print(teks.endswith("apel"))